function sortearCarta() {
  var numeroCartaMaquina = parseInt(Math.random() * 21);
  cartaMaquina = cartas[numeroCartaMaquina];

  divCartaMaquina.style.opacity = 1;
  divCartaJogador.style.opacity = 1;
  divImagemVersus.style.opacity = 1;

  var numeroCartaJogador = parseInt(Math.random() * 21);
  while (numeroCartaJogador == numeroCartaMaquina) {
    numeroCartaJogador = parseInt(Math.random() * 21);
  }
  cartaJogador = cartas[numeroCartaJogador];
  console.log(cartaJogador);

  document.getElementById("btnSortear").disabled = true;
  document.getElementById("btnJogar").disabled = false;
  document.getElementById("btnReset").disabled = false;

  exibirCartaJogador();
  exibirCartaMaquina();
}

function obtemAtributoSelecionado() {
  var radioAtributos = document.getElementsByName("atributo");

  for (var i = 0; i < radioAtributos.length; i++) {
    if (radioAtributos[i].checked == true) {
      return radioAtributos[i].value;
    }
  }
}

function ocultar() {
  document.getElementById("resultado").style.display = "none";
}

function exibir() {
  document.getElementById("resultado").style.display = "block";
}

function jogar() {
  var atributoSelecionado = obtemAtributoSelecionado();
  var divResultado = document.getElementById("resultado");

  if (
    cartaJogador.atributos[atributoSelecionado] >
    cartaMaquina.atributos[atributoSelecionado]
  ) {
    htmlDivResultado =
      "<p class='resultado-final'>Você venceu, " +
      cartaJogador.nome +
      " derrotou " +
      cartaMaquina.nome +
      "</p>";
  } else if (
    cartaMaquina.atributos[atributoSelecionado] >
    cartaJogador.atributos[atributoSelecionado]
  ) {
    htmlDivResultado =
      "<p class='resultado-final'>Você perdeu " +
      cartaMaquina.nome +
      " derrotou você</p>";
  } else {
    htmlDivResultado =
      "<p class='resultado-final'><strong>EMPATE</strong> " +
      cartaJogador.nome +
      " e " +
      cartaMaquina.nome +
      " lutaram até ficarem sem forças e ninguém venceu</p>";
  }
  divResultado.innerHTML = htmlDivResultado;
  document.getElementById("btnJogar").disabled = true;
  document.getElementById("carta-jogador").disabled = false;
  document.getElementById("resultado").disabled = false;
  exibirAtributosMaquina();
  exibir();
}

function exibirCartaJogador() {
  var divCartaJogador = document.getElementById("carta-jogador");
  divCartaJogador.style.backgroundImage = `url(${cartaJogador.imagem})`;
  var tagHTML = "<div id= 'opcoes' class='carta-status'>";
  var opcoesTexto = "";

  for (var atributo in cartaJogador.atributos) {
    opcoesTexto +=
      "<input type='radio' id= 'atributos' name='atributo' value='" +
      atributo +
      "'>" +
      atributo +
      " " +
      cartaJogador.atributos[atributo] +
      "</p>";
    var nome = `<p class="carta-subtitle">${cartaJogador.nome}</p>`;
    divCartaJogador.innerHTML =
      moldura + nome + tagHTML + opcoesTexto + "</div>";
  }
}

